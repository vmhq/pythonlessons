'''
Este es un código Python básico que utiliza una estructura de bucle for para recorrer una lista de números 
y luego imprimir cada número.

Desglose:

numeros = [1, 2, 3]: Aquí estamos definiendo una lista llamada numeros que contiene tres elementos: 1, 2 y 3.

for numero in numeros:: Aquí comenzamos un bucle for. La sintaxis for numero in numeros significa: 
"Para cada numero en la lista numeros..."

print(numero): Esto es lo que queremos hacer con cada numero en la lista numeros. 
En este caso, simplemente estamos imprimiendo el número.

'''

#Se establece la lista
numeros = [1, 2, 3]

#Bucle for
for numero in numeros:
    print(numero)
